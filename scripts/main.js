function startImageCycle(){
    let len=images.length;
    var imageseq = document.getElementById("Startimag")
    let cardNumber =  Math.floor(len * Math.random());
    curImageIndex++;
    if (curImageIndex>52)
    {
        imageseq.src="http://127.0.0.1:5502/PNG-cards-1.3/PNG-cards-1.3/black joker.png"
        speech.text="End of the game"
        window.speechSynthesis.speak(speech)
        sleep(2000);
    setTimeout("location.reload(true);",1500)
    }
    else{
        speech.text=imagessound[cardNumber]
        imageseq.src=images[cardNumber]
        window.speechSynthesis.speak(speech);
        const index = images.indexOf(images[cardNumber]);
        if (index > -1) { // only splice array when item is found
            images.splice(index, 1);
            imagessound.splice(index, 1); // 2nd parameter means remove one item only
          }

    }
    function sleep(milliseconds) {
        const date = Date.now();
        let currentDate = null;
        do {
          currentDate = Date.now();
        } while (currentDate - date < milliseconds);
      }
          
    

}



